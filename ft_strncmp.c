int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	m;

	m = 0;
	while (*s1 == *s2 && m < n - 1 && *s1 && *s2)
	{
		s1++;
		s2++;
		m++;
	}
	if (*s1 != *s2)
		return (*s1 - *s2);
	return (0);
}